public class CharacterModel
{
    private float walkSpeed = 5f;
    private float maxSpeed = 10f;
    private float swingSpeed = 10f;
    private float maxSwingSpeed = 20f;
    private float swingLength = 50f;
    private float jumpForce = 10f;

    public float WalkSpeed { get => walkSpeed; }
    public float MaxSpeed { get => maxSpeed; }
    public float SwingSpeed { get => swingSpeed; }
    public float SwingLength { get => swingLength; }
    public float JumpForce { get => jumpForce; }
    public float MaxSwingSpeed { get => maxSwingSpeed; }

}
